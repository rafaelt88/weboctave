<h2><a href="http://www.gnu.org/software/octave" target="_blank">¿Qué es Octave?</a></h2>
<blockquote>
<p>
GNU Octave es un lenguaje de alto nivel destinado para el cálculo numérico. Provee una interfaz sencilla, orientada a la línea de comandos (consola), que permite la resolución de
problemas numéricos, lineales y no lineales, además permite la ejecución de scripts y puede ser usado como lenguaje orientado al procesamiento por lotes.
Octave nació alrededor del año 1988, y fue concebido originalmente para ser usado en un curso de diseño de reactores químicos para los alumnos de Ingeniería Química de la
Universidad de Texas y la Universidad de Wisconsin-Madison.
</p>
<br></br>
<p>
Octave posee una gran cantidad de herramientas que permiten resolver problemas de algebra lineal, cálculo de raices de ecuaciones no lineales, integración de funciones
ordinarias, manipulaión de polinomios, integración de ecuaciones diferenciales ordinarias y ecuaciones diferenciales algebraicas. Sus funciones también se pueden extender
mediante funciones definidas por el usuario escritas en el lenguaje propio de Octave o usando mádulos dinámicamente cargados escritos en lenguajes como C, C++ y Fortran entre otros.
</p>
</blockquote>
